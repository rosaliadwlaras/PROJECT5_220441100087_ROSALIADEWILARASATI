    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbldata.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih baris yang ingin dihapus.");
        } else {
            int option = JOptionPane.showConfirmDialog(null,
                "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                try {
                    String kodeToDelete = (String) tbldata.getValueAt(selectedRow, 0);
                    String sql = "DELETE FROM data WHERE Kode = ?";
                    java.sql.Connection conn = (java.sql.Connection)project5.PROJECT5.koneksiDB();
                    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                    pst.setString(1, kodeToDelete);
                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                    tampil_data();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gagal menghapus data.");
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

