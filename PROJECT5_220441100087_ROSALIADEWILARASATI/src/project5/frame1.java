    public void tampil_data(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Kode");
        tabel.addColumn("Barang");
        tabel.addColumn("Harga");
        tabel.addColumn("Nama");
        tabel.addColumn("Jumlah");
        tabel.addColumn("Total Harga");
        tabel.addColumn("Bayar");
        tabel.addColumn("Kembalian");
        
        try{
            java.sql.Connection conn = project5.PROJECT5.koneksiDB();
            String query = "select * from data";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            
            while (rs.next()){
                Object[] row = {rs.getString(1), rs.getString(2),
                    rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)
                , rs.getString(7), rs.getString(8)};
                tabel.addRow(row);
            }
            tbldata.setModel(tabel);
        } catch (Exception e){
            e.printStackTrace();
        }
    }
